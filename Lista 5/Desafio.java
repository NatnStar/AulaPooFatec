package Funcoes;

public class Desafio {

	public static void main(String[] args) {
		
        int resultado = somar(10, 20);
        mostrar(resultado);
    }

    static int somar(int a, int b) {
        return a + b;
    }

    static void mostrar(int r) {
        System.out.println(r);
    }


}
